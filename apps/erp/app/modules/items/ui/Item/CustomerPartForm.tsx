import { ValidatedForm } from "@carbon/form";
import { useTranslation } from "@carbon/locale";
import {
  Button,
  Drawer,
  DrawerBody,
  DrawerContent,
  DrawerFooter,
  DrawerHeader,
  DrawerTitle,
  HStack,
  VStack
} from "@carbon/react";
import { useNavigate, useParams } from "react-router";
import type { z } from "zod/v3";
import { Customer, Hidden, Input, Submit } from "~/components/Form";
import { usePermissions } from "~/hooks";
import { path } from "~/utils/path";
import { customerPartValidator } from "../../items.models";

type CustomerPartFormProps = {
  initialValues: z.infer<typeof customerPartValidator> & {
    readableId: string;
  };
};

const CustomerPartForm = ({ initialValues }: CustomerPartFormProps) => {
  const { t } = useTranslation(["items", "common"]);
  const permissions = usePermissions();
  const navigate = useNavigate();
  const { itemId } = useParams();
  if (!itemId) throw new Error("itemId not found");

  const isEditing = initialValues.id !== undefined;
  const isDisabled = isEditing
    ? !permissions.can("update", "parts")
    : !permissions.can("create", "parts");

  const onClose = () => navigate(-1);

  return (
    <Drawer
      open
      onOpenChange={(open) => {
        if (!open) onClose();
      }}
    >
      <DrawerContent>
        <ValidatedForm
          defaultValues={initialValues}
          validator={customerPartValidator}
          method="post"
          action={isEditing ? undefined : path.to.newCustomerPart(itemId)}
          className="flex flex-col h-full"
        >
          <DrawerHeader>
            <DrawerTitle>
              {isEditing ? t("items:editCustomerPart") : t("items:newCustomerPart")}
            </DrawerTitle>
          </DrawerHeader>
          <DrawerBody>
            <Hidden name="id" />
            <Hidden name="itemId" />

            <VStack spacing={4}>
              <Input name="readableId" label={t("items:partId")} isDisabled />
              <Customer name="customerId" label={t("items:customer")} />
              <Input name="customerPartId" label={t("items:customerPartId")} />
              <Input
                name="customerPartRevision"
                label={t("items:customerPartRevision")}
              />
            </VStack>
          </DrawerBody>
          <DrawerFooter>
            <HStack>
              <Submit isDisabled={isDisabled}>{t("common:save")}</Submit>
              <Button size="md" variant="solid" onClick={onClose}>
                {t("common:cancel")}
              </Button>
            </HStack>
          </DrawerFooter>
        </ValidatedForm>
      </DrawerContent>
    </Drawer>
  );
};

export default CustomerPartForm;
